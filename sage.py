hard_assert(df_traj["SoH_SAGE_Truth"].notna().all(), "SoH_SAGE_Truth contains NaNs; fix upstream export.")

# -------------------------------------------------------------------------
# 2. Extract parameters using accessor helper
# -------------------------------------------------------------------------
cap_nom = get_param(params, "capacity_kwh")           # 5000 kWh
P_nom = get_param(params, "P_dis")                    # 1000 kW
H = get_param(params, "num_discharge_hours")          # 4 hours
soh_eol = get_param(params, "SoH_EOL")                # 0.70

# SOC window parameters
SoC_max_BOL = get_param(params, "SoC_max_BOL")        # 0.95
SoC_max_EOL = get_param(params, "SoC_max_EOL")        # 0.80
SoC_min_BOL = get_param(params, "SoC_min_BOL")        # 0.05
SoC_min_EOL = get_param(params, "SoC_min_EOL")        # 0.20

# Efficiency parameters
eta_dis_BOL = get_param(params, "eta_discharge_bol")  # 0.95
eta_dis_EOL = get_param(params, "eta_discharge_eol")  # 0.90

# -------------------------------------------------------------------------
# 3. Extract SoH trajectory
# -------------------------------------------------------------------------
soh_vector = df_traj["SoH_SAGE_Truth"].values
years_vector = df_traj["years"].values

# BOL reference for normalization (use actual month-0 SoH, not 1.0)
soh_bol = soh_vector[0]
hard_assert(soh_bol > soh_eol, f"BOL SoH ({soh_bol:.4f}) must exceed EOL threshold ({soh_eol})")

# -------------------------------------------------------------------------
# 4. Compute degradation progress (0.0 at BOL, 1.0 at EOL)
# -------------------------------------------------------------------------
# Key: reference BOL SoH, not 1.0, so deg_progress=0 at month 0
deg_progress = np.clip((soh_bol - soh_vector) / (soh_bol - soh_eol), 0.0, 1.0)

# -------------------------------------------------------------------------
# 5. SOC window contraction (Equations 6-7)
# -------------------------------------------------------------------------
soc_max_vec = SoC_max_BOL - (SoC_max_BOL - SoC_max_EOL) * deg_progress
soc_min_vec = SoC_min_BOL + (SoC_min_EOL - SoC_min_BOL) * deg_progress
window_width = soc_max_vec - soc_min_vec

# Guard against invalid SOC window (would indicate param misconfiguration)
hard_assert(np.all(window_width >= -1e-12), "SOC window width went negative (SoC_min > SoC_max). Check params.")
window_width = np.clip(window_width, 0.0, 1.0)

# -------------------------------------------------------------------------
# 6. Nominal capacity remaining
# -------------------------------------------------------------------------
nominal_cap_remaining = cap_nom * soh_vector  # kWh

# -------------------------------------------------------------------------
# 7. Available energy in SOC window
# -------------------------------------------------------------------------
E_avail = window_width * nominal_cap_remaining  # kWh

# -------------------------------------------------------------------------
# 8. Efficiency fade (Equation 17)
# -------------------------------------------------------------------------
eta_dis_vec = eta_dis_BOL - (eta_dis_BOL - eta_dis_EOL) * deg_progress

# -------------------------------------------------------------------------
# 9. Firm power calculation (Equation 4)
# -------------------------------------------------------------------------
P_energy_limited = E_avail * eta_dis_vec / H  # kW

# -------------------------------------------------------------------------
# 10. Apply nameplate power constraint (thermal derating omitted)
# -------------------------------------------------------------------------
P_firm = np.minimum(P_energy_limited, P_nom)  # kW

# -------------------------------------------------------------------------
# 11. Usable energy
# -------------------------------------------------------------------------
usable_energy_kwh = P_firm * H  # kWh

# -------------------------------------------------------------------------
# 12. Normalize to BOL values
# -------------------------------------------------------------------------
soh_norm = soh_vector / soh_bol
usable_energy_norm = usable_energy_kwh / usable_energy_kwh[0]

# -------------------------------------------------------------------------
