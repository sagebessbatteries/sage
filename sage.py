"""
SAGE Cell 9: Single-Asset Simulation Runner
============================================
Simulates one battery asset through the full simulation horizon.
Samples heterogeneity parameters from asset_seed, runs year-by-year
until EOL or horizon end. Section 3.11.

FIX: Month indexing now uses simulation time, not calendar timestamps,
to avoid leap year drift causing revenue overwrites.
"""

import numpy as np
import pandas as pd
from dataclasses import replace

require_global("sage_params")
require_global("SAGEState")
require_global("sage_simulate_trajectory")

EXPORT_YEARS = sage_params.export_years

print(f"Cell 9: EXPORT_YEARS = {EXPORT_YEARS}")


def build_simulation_horizon(env_base_seq, cmd_base_seq, total_hours_needed):
    """Tile base sequences to cover full simulation horizon."""
    hard_assert(len(env_base_seq) == len(cmd_base_seq), "env/cmd base lengths must match")
    base_len = len(env_base_seq)
    hard_assert(base_len > 0, "base sequences must be non-empty")

    if not hasattr(env_base_seq[0], "t_timestamp"):
        raise AttributeError("env_base_seq[0] must have 't_timestamp' attribute")

    env_out, cmd_out = [], []
    for k in range(int(total_hours_needed)):
        j = k % base_len
        cycle_idx = k // base_len
        base_step = env_base_seq[j]
        new_ts = pd.Timestamp(base_step.t_timestamp) + pd.Timedelta(hours=cycle_idx * base_len)
        env_out.append(replace(base_step, t_timestamp=new_ts))
        cmd_out.append(cmd_base_seq[j])
    return env_out, cmd_out


def simulate_single_asset(
    asset_id,
    env_base_seq,
    cmd_base_seq,
    *,
    total_hours_needed,
    sim_years,
    hours_per_year,
    cap_nom,
    soc_min,
    soc_max,
    eol_threshold,
    T_amb_mean,
    init_soc,
    grad_scale,
    sigma_qual,
    asset_seed: int = None,
):
    """
    Simulate one asset through full horizon. Section 3.11.

    Samples quality_factor and vertical_pos from asset_seed.
    Returns (summary_dict, monthly_histories, export_bundle).

    export_bundle contains EXPORT_YEARS years of data for TwinBESS.
    """
    if asset_seed is None:
        asset_seed = int(asset_id)
    rng = np.random.default_rng(asset_seed)

    q_factor = float(rng.normal(loc=1.0, scale=float(sigma_qual)))
    while q_factor <= 0.0:
        q_factor = float(rng.normal(loc=1.0, scale=float(sigma_qual)))

    g_factor = float(rng.uniform(0.0, 1.0))
    temp_offset = g_factor * grad_scale

    env_long, cmd_long = build_simulation_horizon(env_base_seq, cmd_base_seq, total_hours_needed)

    current_state = SAGEState(
        soc_true=float(init_soc),
        soh_true=1.0,
        q_loss_cal=0.0,
        q_loss_cyc=0.0,
        T_cell=float(T_amb_mean),
        cumulative_kwh=0.0,
        t_index=0,
        t_cal_cumulative=0.0,
    )

    target_months = sim_years * 12
    monthly_soh = [float("nan")] * target_months
    monthly_T_mean = [float("nan")] * target_months
    monthly_qcal = [float("nan")] * target_months
    monthly_qcyc = [float("nan")] * target_months
    monthly_cumulative_kwh = [float("nan")] * target_months
    monthly_cumulative_revenue = [float("nan")] * target_months
    T_sum = [0.0] * target_months
    T_count = [0] * target_months

    alive = True
    death_year = float(sim_years)
    lifetime_revenue = 0.0
    soc_eps = float(np.finfo(float).eps)
    total_simulated_hours = 0

    export_traj = []
    export_env = []
    export_cmd = []

    for year in range(sim_years):
        if not alive:
            break
        if current_state.soh_true <= eol_threshold:
            alive = False
            death_year = float(year)
            break

        start = year * hours_per_year
        stop = start + hours_per_year
        env_year = env_long[start:stop]
        cmd_year = cmd_long[start:stop]
        hard_assert(len(env_year) == hours_per_year, f"Year slice error: len={len(env_year)} at year={year}")

        traj, next_state, _ = sage_simulate_trajectory(
            current_state,
            env_year,
            cmd_year,
            sage_params,
            gradient_factor=g_factor,
            quality_factor=q_factor,
            verbose=False,
        )

        n_sim = len(traj)
        hard_assert(n_sim > 0, f"Empty trajectory at asset={asset_id}, year={year}")
        total_simulated_hours += n_sim

        if year < EXPORT_YEARS:
            export_traj.extend(traj)
            export_env.extend(env_year[:n_sim])
            export_cmd.extend(cmd_year[:n_sim])

        if n_sim < hours_per_year:
            alive = False
            death_year = float(year) + (n_sim / float(hours_per_year))

        for t in range(n_sim):
            state = traj[t]
            env_step = env_year[t]
            cmd_step = cmd_year[t]

            # FIXED: Use simulation time for month indexing, not calendar timestamps
            # This avoids leap year drift causing month overwrites
            month_within_year = (t * 12) // hours_per_year
            global_month = year * 12 + month_within_year
            hard_assert(0 <= global_month < target_months, f"global_month out of range: {global_month}")

            p_cmd = float(cmd_step.P_dispatch_kw)
            is_empty = state.soc_true <= (soc_min + soc_eps)
            is_full = state.soc_true >= (soc_max - soc_eps)

            actual_power = p_cmd
            if p_cmd > 0.0 and is_empty:
                actual_power = 0.0
            if p_cmd < 0.0 and is_full:
                actual_power = 0.0
            lifetime_revenue += env_step.p_market * (actual_power / 1000.0)

            T_cell = float(state.T_cell)
            hard_assert(np.isfinite(T_cell), f"Non-finite T_cell at asset={asset_id}, year={year}, t={t}")
            T_sum[global_month] += T_cell
            T_count[global_month] += 1

            monthly_soh[global_month] = float(state.soh_true)
            monthly_qcal[global_month] = float(state.q_loss_cal)
            monthly_qcyc[global_month] = float(state.q_loss_cyc)
            monthly_cumulative_kwh[global_month] = float(state.cumulative_kwh)
            monthly_cumulative_revenue[global_month] = lifetime_revenue

        current_state = SAGEState(
            soc_true=float(next_state.soc_true),
            soh_true=float(next_state.soh_true),
            q_loss_cal=float(next_state.q_loss_cal),
            q_loss_cyc=float(next_state.q_loss_cyc),
            T_cell=float(next_state.T_cell),
            cumulative_kwh=float(next_state.cumulative_kwh),
            t_index=0,
            t_cal_cumulative=float(next_state.t_cal_cumulative),
        )

    for m in range(target_months):
        if T_count[m] > 0:
            monthly_T_mean[m] = T_sum[m] / float(T_count[m])

    lifetime_cycles = current_state.cumulative_kwh / cap_nom

    export_bundle = (export_traj, export_env, export_cmd)
    hard_assert(len(export_traj) > 0, "export_traj not captured")

    asset_summary = {
        "id": asset_id,
        "quality_factor": q_factor,
        "vertical_pos": g_factor,
        "temp_offset_applied": temp_offset,
        "final_soh": current_state.soh_true,
        "final_T_cell": current_state.T_cell,
        "revenue": lifetime_revenue,
        "cycles": lifetime_cycles,
        "lifespan_years": death_year,
        "final_t_cal_cumulative": current_state.t_cal_cumulative,
        "final_elapsed_hours": total_simulated_hours,
        "asset_seed": asset_seed,
    }

    monthly_histories = {
        "monthly_soh_history": monthly_soh,
        "monthly_T_cell_mean_history": monthly_T_mean,
        "monthly_qcal_history": monthly_qcal,
        "monthly_qcyc_history": monthly_qcyc,
        "monthly_cumulative_kwh_history": monthly_cumulative_kwh,
        "monthly_cumulative_revenue_history": monthly_cumulative_revenue,
    }

    return asset_summary, monthly_histories, export_bundle


