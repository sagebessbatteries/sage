import numpy as np


def require_global(name: str):
    """
    Verify that a required global variable exists.

    Parameters
    ----------
    name : str
        Name of the global variable to check.

    Returns
    -------
    object
        The value of the global variable.

    Raises
    ------
    NameError
        If the global variable does not exist.
    """
    if name not in globals():
        raise NameError(f"Required global '{name}' not found. Run earlier cells first.")
    return globals()[name]


def hard_assert(condition: bool, message: str) -> None:
    """
    Assert a condition, raising ValueError if false.

    Parameters
    ----------
    condition : bool
        Condition to check.
    message : str
        Error message if condition is false.

    Raises
    ------
    ValueError
        If condition is false.
    """
    if not condition:
        raise ValueError(message)


