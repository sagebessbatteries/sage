"""
SAGE Cell 7: Temperature-Aware Merchant Dispatch (TwinBESS)
============================================================
Dispatch scheduler that uses day-ahead OUTDOOR temperature forecasts to select
optimal discharge windows, capturing the temperature-price correlation
that exists in real markets like ERCOT.

DUAL TEMPERATURE MODEL:
- Uses T_outdoor_forecast (day-ahead outdoor weather) for dispatch decisions
- This drives CDD/HDD which correlates with electricity prices
- Container temperature (T_amb_forecast) is NOT used here

Key Difference from SAGE Cell 7
-------------------------------
-
This creates learnable structure for the SoH forecasting model:
- Hot days ---> later discharge (evening ramp after solar)
- Cold days ---> earlier discharge (morning heating peak)
- Mild days ---> standard afternoon peak
"""

import numpy as np
import pandas as pd

require_global("sage_params")
require_global("generate_sage_environment")
require_global("SAGECommand")
require_global("HOURS_PER_YEAR")
require_global("HOURS_PER_DAY")

sp = sage_params

# -------------------------------------------------------------------------
# Base dispatch parameters
# -------------------------------------------------------------------------
dur = sp.num_discharge_hours
P_dis_kw = sp.P_dis
dispatch_window_start = sp.dispatch_window_start
dispatch_window_end = sp.dispatch_window_end
random_seed = sp.random_seed
sim_years = sp.simulation_years

# -------------------------------------------------------------------------
# Temperature-aware shifting parameters
# -------------------------------------------------------------------------
T_balance = sp.T_BPT
cdd_shift_threshold = sp.cdd_shift_threshold
hdd_shift_threshold = sp.hdd_shift_threshold
hot_day_peak_shift = sp.hot_day_peak_shift
cold_day_peak_shift = sp.cold_day_peak_shift

# -------------------------------------------------------------------------
# Generate environment
# -------------------------------------------------------------------------
n_hours = sim_years * HOURS_PER_YEAR

env_ref = generate_sage_environment(n_hours, sp, seed=random_seed, verbose=True)

# -------------------------------------------------------------------------
# Build DataFrame with OUTDOOR forecast temperature (day-ahead information)
# This is the key change: use T_outdoor_forecast, not T_amb_forecast
# -------------------------------------------------------------------------
env_df = pd.DataFrame({
    "ts": [e.t_timestamp for e in env_ref],
    "p_forecast": [e.p_forecast for e in env_ref],
    "T_outdoor_forecast": [e.T_outdoor_forecast for e in env_ref],
})
env_df["date"] = env_df["ts"].dt.floor("D")
env_df["hour"] = env_df["ts"].dt.hour

# -------------------------------------------------------------------------
# Compute daily forecast CDD/HDD (using day-ahead OUTDOOR temperature forecast)
# -------------------------------------------------------------------------
daily_stats = env_df.groupby("date").agg(
    T_forecast_mean=("T_outdoor_forecast", "mean"),
).reset_index()

daily_stats["forecast_cdd"] = np.maximum(daily_stats["T_forecast_mean"] - T_balance, 0.0)
daily_stats["forecast_hdd"] = np.maximum(T_balance - daily_stats["T_forecast_mean"], 0.0)

# Classify day type based on forecast
def classify_day(row):
    if row["forecast_cdd"] > cdd_shift_threshold:
        return "hot"
    elif row["forecast_hdd"] > hdd_shift_threshold:
        return "cold"
    else:
        return "mild"

daily_stats["day_type"] = daily_stats.apply(classify_day, axis=1)

# Merge back to hourly data
env_df = env_df.merge(daily_stats[["date", "day_type", "forecast_cdd", "forecast_hdd"]], on="date")

# -------------------------------------------------------------------------
# Temperature-aware dispatch scheduling
# -------------------------------------------------------------------------
cmd_seq = []
audit_rows = []

kernel = np.ones(dur) / dur

# Base valid start hour range
valid_start_min_base = dispatch_window_start
valid_start_max_base = dispatch_window_end - dur

n_hot_days = 0
n_cold_days = 0
n_mild_days = 0

for date, df_day in env_df.groupby("date", sort=True):
    prices = df_day["p_forecast"].to_numpy()
    day_type = df_day["day_type"].iloc[0]
    forecast_cdd = df_day["forecast_cdd"].iloc[0]
    forecast_hdd = df_day["forecast_hdd"].iloc[0]

    hard_assert(len(prices) == HOURS_PER_DAY, f"Day {date} has {len(prices)} hours, expected {HOURS_PER_DAY}")

    # Shift dispatch window based on day type
    if day_type == "hot":
        # Hot day: shift window later (evening ramp after solar)
        window_shift = hot_day_peak_shift
        n_hot_days += 1
    elif day_type == "cold":
        # Cold day: shift window earlier (morning heating peak)
        window_shift = cold_day_peak_shift
        n_cold_days += 1
    else:
        # Mild day: no shift
        window_shift = 0
        n_mild_days += 1

    # Apply shift to valid window (clamped to [0, 24-dur])
    valid_start_min = max(0, valid_start_min_base + window_shift)
    valid_start_max = min(HOURS_PER_DAY - dur, valid_start_max_base + window_shift)

    # Ensure valid window
    if valid_start_max < valid_start_min:
        valid_start_max = valid_start_min

    # Compute rolling average for all possible start hours
    rolling = np.convolve(prices, kernel, mode="valid")

    # Mask invalid start hours
    rolling_constrained = rolling.copy()
    rolling_constrained[:valid_start_min] = -np.inf
    if valid_start_max + 1 < len(rolling_constrained):
        rolling_constrained[valid_start_max + 1:] = -np.inf

    d_s = int(np.argmax(rolling_constrained))
    d_e = d_s + dur

    discharge_h = 0

    for h in range(HOURS_PER_DAY):
        if d_s <= h < d_e:
            P = P_dis_kw
            discharge_h += 1
        else:
            P = 0.0

        cmd_seq.append(SAGECommand(P_dispatch_kw=P))

    audit_rows.append({
        "date": date,
        "day_type": day_type,
        "forecast_cdd": forecast_cdd,
        "forecast_hdd": forecast_hdd,
        "window_shift": window_shift,
        "valid_start_min": valid_start_min,
        "valid_start_max": valid_start_max,
        "discharge_start": d_s,
        "discharge_hours": discharge_h,
        "avg_price_forecast": float(rolling[d_s]),
    })

cmd_seq_ref = cmd_seq

# -------------------------------------------------------------------------
# Schedule audit
# -------------------------------------------------------------------------
audit_df = pd.DataFrame(audit_rows)

days_total = len(audit_df)
days_any_dis = int(np.sum(audit_df["discharge_hours"] > 0))

print("\nCell 7: Temperature-Aware Dispatch Schedule (TwinBESS)")
print("=" * 60)
print(f"   Simulation horizon:    {sim_years} years ({n_hours} hours)")
print(f"   Base dispatch window:  {dispatch_window_start}:00 - {dispatch_window_end}:00")
print(f"   Random seed:           {random_seed}")
print(f"")
print(f"   === Day Classification (using T_outdoor_forecast) ===")
print(f"   Hot days (CDD > {cdd_shift_threshold}):  {n_hot_days} ({100*n_hot_days/days_total:.1f}%) -> window +{hot_day_peak_shift}h")
print(f"   Cold days (HDD > {hdd_shift_threshold}): {n_cold_days} ({100*n_cold_days/days_total:.1f}%) -> window {cold_day_peak_shift:+d}h")
print(f"   Mild days:              {n_mild_days} ({100*n_mild_days/days_total:.1f}%) -> no shift")
print(f"")
print(f"   === Dispatch Summary ===")
print(f"   Total days:            {days_total}")
print(f"   Days w/ discharge:     {days_any_dis}")

# Discharge start hour distribution by day type
print(f"")
print(f"   === Discharge Start Hour by Day Type ===")
for dt in ["hot", "cold", "mild"]:
    subset = audit_df[audit_df["day_type"] == dt]
    if len(subset) > 0:
        mean_start = subset["discharge_start"].mean()
        print(f"   {dt.capitalize():5s}: mean start hour = {mean_start:.1f}")

# Verify discharge duration
bad = audit_df[audit_df["discharge_hours"] != dur]
hard_assert(len(bad) == 0,
    f"Fixed-duration discharge contract violated:\n{bad.head(5).to_string(index=False)}")

print(f"")
print(f"   PASS: exactly one {dur}-hour discharge block per day")
print("=" * 60)









