"""
SAGE Cell 4: Environment Generator
==================================
Generates the exogenous market and weather environment for simulation.

DUAL TEMPERATURE MODEL:
- T_outdoor: Full Texas weather swings ---> CDD/HDD ---> electricity prices
- T_ambient: Container temperature (derived from outdoor with HVAC attenuation) ---> battery degradation

CONTAINER TEMPERATURE MODEL (REFACTORED):
- T_ambient = container_baseline + container_attenuation * (T_outdoor - T_outdoor_mean)
- container_attenuation = 0: Perfect HVAC (Phoenix = Seattle)
- container_attenuation = 1: No HVAC (container tracks outdoor)

VOLATILITY FIX: Noise now scales with weather-adjusted price level,
reflecting the empirical observation that electricity price volatility
increases during high-demand (high-price) periods.

Outputs
-------
generate_sage_environment : function
    Creates list of SAGEEnvironmentStep for n_hours.
"""

import numpy as np
import pandas as pd
from typing import List

require_global("sage_params")
require_global("SAGEEnvironmentStep")
require_global("HOURS_PER_YEAR")
require_global("HOURS_PER_DAY")
require_global("DAYS_PER_YEAR")


def generate_sage_environment(
    n_hours: int,
    sp,  # SAGEParams
    seed: int = 42,
    verbose: bool = False
) -> List[SAGEEnvironmentStep]:
    """
    Generate exogenous environment: prices and dual temperatures.

    Parameters
    ----------
    n_hours : int
        Number of simulation hours.
    sp : SAGEParams
        Validated parameter object.
    seed : int
        Random seed for reproducibility.
    verbose : bool
        Print generation statistics.

    Returns
    -------
    List[SAGEEnvironmentStep]
        One entry per simulation hour.
    """
    rng = np.random.default_rng(seed)

    # -------------------------------------------------------------------------
    # Time index
    # -------------------------------------------------------------------------
    start = pd.Timestamp(sp.simulation_start_date)
    times = pd.date_range(start=start, periods=n_hours, freq="h")
    hour_of_day = times.hour.values
    day_of_year = times.dayofyear.values
    month = times.month.values

    # -------------------------------------------------------------------------
    # OUTDOOR Temperature (Texas weather - large swings)
    # Drives: CDD/HDD ---> electricity prices
    # -------------------------------------------------------------------------
    seasonal_outdoor = sp.T_outdoor_amp_seasonal * np.sin(
        2 * np.pi * (day_of_year - sp.seasonal_peak_day) / DAYS_PER_YEAR
    )
    diurnal_outdoor = sp.T_outdoor_amp_diurnal * np.sin(
        2 * np.pi * (hour_of_day - sp.T_phase_shift_hours) / HOURS_PER_DAY
    )
    noise_outdoor = rng.normal(0, sp.sigma_outdoor_noise, n_hours)

    T_outdoor = sp.T_outdoor_mean + seasonal_outdoor + diurnal_outdoor + noise_outdoor

    # Day-ahead outdoor forecast (what operator knows evening before)
    forecast_error_outdoor = rng.normal(0, sp.sigma_outdoor_forecast, n_hours)
    T_outdoor_forecast = T_outdoor + forecast_error_outdoor

    # -------------------------------------------------------------------------
    # CONTAINER Temperature - derived from outdoor with HVAC attenuation
    #
    # This model captures HVAC behavior:
    # - container_baseline: HVAC setpoint (e.g., 22degC)
    # - container_attenuation: fraction of outdoor deviation that penetrates
    #   - 0.0 = perfect HVAC (container always at setpoint)
    #   - 0.1 = 10% of outdoor swings penetrate
    #   - 1.0 = no HVAC (container tracks outdoor exactly)
    #
    # Physical interpretation: Even well-designed HVAC systems have thermal
    # mass, imperfect insulation, and control lag that allow some outdoor
    # influence. The attenuation factor captures this in aggregate.
    # -------------------------------------------------------------------------
    T_outdoor_deviation = T_outdoor - sp.T_outdoor_mean
    T_ambient = sp.container_baseline + sp.container_attenuation * T_outdoor_deviation

    # Small HVAC control noise (setpoint hunting, sensor noise, door openings)
    T_ambient = T_ambient + rng.normal(0, sp.sigma_temp_noise, n_hours)

    # Day-ahead container forecast (derived from outdoor forecast)
    T_outdoor_forecast_deviation = T_outdoor_forecast - sp.T_outdoor_mean
    T_amb_forecast = sp.container_baseline + sp.container_attenuation * T_outdoor_forecast_deviation
    T_amb_forecast = T_amb_forecast + rng.normal(0, sp.sigma_temp_forecast, n_hours)

    # -------------------------------------------------------------------------
    # CDD / HDD from OUTDOOR temperature (not container!)
    # -------------------------------------------------------------------------
    CDD = np.maximum(0, T_outdoor - sp.T_BPT)
    HDD = np.maximum(0, sp.T_BPT - T_outdoor)

    # Forecast CDD/HDD for price forecasting
    CDD_forecast = np.maximum(0, T_outdoor_forecast - sp.T_BPT)
    HDD_forecast = np.maximum(0, sp.T_BPT - T_outdoor_forecast)

    # -------------------------------------------------------------------------
    # Base price profile with seasonal scaling
    # -------------------------------------------------------------------------
    base_profile = np.array(sp.base_price_profile)
    monthly_means = np.array(sp.price_mean_monthly)
    monthly_vols = np.array(sp.target_volatility_monthly)

    # Scale base profile by monthly mean
    annual_base_mean = np.mean(base_profile)
    monthly_scale = monthly_means[month - 1] / annual_base_mean

    # -------------------------------------------------------------------------
    # Peak hour shifting based on OUTDOOR temperature
    # -------------------------------------------------------------------------
    daily_cdd = np.zeros(n_hours)
    daily_hdd = np.zeros(n_hours)

    for d in range(n_hours // HOURS_PER_DAY):
        start_idx = d * HOURS_PER_DAY
        end_idx = start_idx + HOURS_PER_DAY
        daily_cdd[start_idx:end_idx] = np.mean(CDD[start_idx:end_idx])
        daily_hdd[start_idx:end_idx] = np.mean(HDD[start_idx:end_idx])

    # Shift factor: positive shifts peak later (hot), negative earlier (cold)
    shift_hours = np.zeros(n_hours)
    hot_mask = daily_cdd > sp.cdd_shift_threshold
    cold_mask = daily_hdd > sp.hdd_shift_threshold
    shift_hours[hot_mask] = sp.hot_day_peak_shift
    shift_hours[cold_mask] = sp.cold_day_peak_shift

    # Apply shift to base profile
    shifted_hour = (hour_of_day - shift_hours).astype(int) % HOURS_PER_DAY
    p_base_shifted = base_profile[shifted_hour] * monthly_scale

    # Morning peak on cold days
    morning_peak = np.zeros(n_hours)
    cold_day_mask = daily_hdd > sp.hdd_shift_threshold
    morning_gaussian = np.exp(-0.5 * ((hour_of_day - sp.morning_peak_hour) / sp.morning_peak_width) ** 2)
    morning_peak[cold_day_mask] = sp.morning_peak_magnitude * base_profile[hour_of_day[cold_day_mask]] * monthly_scale[cold_day_mask] * morning_gaussian[cold_day_mask]

    p_base_final = p_base_shifted + morning_peak

    # -------------------------------------------------------------------------
    # Weather adjustment to prices (using OUTDOOR CDD/HDD)
    # -------------------------------------------------------------------------
    weather_adder = sp.beta_weather_cdd * CDD + sp.beta_weather_hdd * HDD
    weather_adder_forecast = sp.beta_weather_cdd * CDD_forecast + sp.beta_weather_hdd * HDD_forecast

    # -------------------------------------------------------------------------
    # Compute weather-adjusted base price (before noise and spikes)
    # This is the price level that volatility should scale with
    # -------------------------------------------------------------------------
    p_base_weather = p_base_final + weather_adder

    # -------------------------------------------------------------------------
    # Volatility noise - SCALED BY ACTUAL PRICE LEVEL
    #
    # Scientific justification: In electricity markets, volatility increases
    # with price level because:
    # 1. High prices occur when system is near capacity
    # 2. Near capacity, the supply curve is steep (convex)
    # 3. Small demand changes cause large price swings
    #
    # The target_volatility_monthly specifies the coefficient of variation
    # (σ/μ) for each month. We apply this CV to the actual weather-adjusted
    # price, not the target mean.
    # -------------------------------------------------------------------------
    monthly_cv = monthly_vols[month - 1]

    # Standard deviation scales with the local price level
    local_sigma = monthly_cv * p_base_weather

    # Generate noise with heteroscedastic (price-dependent) volatility
    price_noise = rng.normal(0, 1, n_hours) * local_sigma

    # -------------------------------------------------------------------------
    # Spikes (temperature-conditional)
    # -------------------------------------------------------------------------
    spike_prob = np.full(n_hours, sp.gamma_spike)

    # Increase spike probability on extreme temperature days
    extreme_hot = CDD > sp.spike_temp_threshold
    extreme_cold = HDD > sp.spike_temp_threshold
    spike_prob[extreme_hot] *= sp.spike_cdd_multiplier
    spike_prob[extreme_cold] *= sp.spike_hdd_multiplier

    spike_occurs = rng.random(n_hours) < spike_prob
    spike_magnitude = np.zeros(n_hours)
    n_spikes = np.sum(spike_occurs)
    if n_spikes > 0:
        spike_magnitude[spike_occurs] = (rng.pareto(sp.pareto_shape, n_spikes) + 1) * sp.spike_base_magnitude

    # -------------------------------------------------------------------------
    # Final prices
    # -------------------------------------------------------------------------
    p_market = p_base_weather + price_noise + spike_magnitude
    p_market = np.clip(p_market, 0, sp.price_ceiling)

    # Forecast prices (no spike knowledge, uses forecast CDD/HDD)
    p_base_weather_forecast = p_base_final + weather_adder_forecast
    local_sigma_forecast = monthly_cv * p_base_weather_forecast
    forecast_noise = rng.normal(0, sp.sigma_forecast_price, n_hours)
    p_forecast = p_base_weather_forecast + forecast_noise
    p_forecast = np.clip(p_forecast, 0, sp.price_ceiling)

    # -------------------------------------------------------------------------
    # Outage flags
    # -------------------------------------------------------------------------
    is_valid = rng.random(n_hours) >= sp.unscheduled_outage_hourly_prob

    # -------------------------------------------------------------------------
    # Build environment list
    # -------------------------------------------------------------------------
    env = [
        SAGEEnvironmentStep(
            t_timestamp=times[i],
            p_market=float(p_market[i]),
            p_forecast=float(p_forecast[i]),
            T_ambient=float(T_ambient[i]),
            T_amb_forecast=float(T_amb_forecast[i]),
            T_outdoor=float(T_outdoor[i]),
            T_outdoor_forecast=float(T_outdoor_forecast[i]),
            is_valid=bool(is_valid[i]),
        )
        for i in range(n_hours)
    ]

    if verbose:
        # Compute container temperature statistics
        T_container_range = T_ambient.max() - T_ambient.min()
        T_outdoor_range = T_outdoor.max() - T_outdoor.min()
        effective_attenuation = T_container_range / T_outdoor_range if T_outdoor_range > 0 else 0

        print(f"Cell 4: Generated {n_hours:,} hours of environment data.")
        print(f"   OUTDOOR temp: {T_outdoor.min():.1f}degC to {T_outdoor.max():.1f}degC (range: {T_outdoor_range:.1f}degC)")
        print(f"   CONTAINER temp: {T_ambient.min():.1f}degC to {T_ambient.max():.1f}degC (range: {T_container_range:.1f}degC)")
        print(f"      Baseline (setpoint): {sp.container_baseline}degC")
        print(f"      Configured attenuation: {sp.container_attenuation*100:.1f}%")
        print(f"      Effective attenuation: {effective_attenuation*100:.1f}%")
        print(f"   Weather-adjusted base price: ${p_base_weather.min():.0f} to ${p_base_weather.max():.0f}/MWh")
        print(f"   Final price range: ${p_market.min():.0f} to ${p_market.max():.0f}/MWh")
        print(f"   Spikes: {n_spikes} ({100*n_spikes/n_hours:.2f}%), base magnitude: ${sp.spike_base_magnitude:.0f}/MWh")
        corr = np.corrcoef(T_outdoor, p_market)[0, 1]
        print(f"   Outdoor temp-price correlation: {corr:.3f}")

        # Report realized vs target statistics
        print(f"   ")
        print(f"   === Calibration Check ===")
        for m in range(1, 13):
            mask = (month == m)
            realized_mean = p_market[mask].mean()
            realized_std = p_market[mask].std()
            realized_cv = realized_std / realized_mean if realized_mean > 0 else 0
            target_mean = monthly_means[m-1]
            target_cv = monthly_vols[m-1]
            print(f"   Month {m:2d}: Mean ${realized_mean:5.1f} (target ${target_mean:5.1f}) | "
                  f"CV {realized_cv:.2f} (target {target_cv:.2f})")

    return env


print("Cell 4: Environment generator defined (with HVAC-derived container temperature).")
print("   generate_sage_environment(n_hours, sp, seed, verbose)")
print("   Container model: T_ambient = container_baseline + container_attenuation × (T_outdoor - T_outdoor_mean)")
print("   container_attenuation=0 ---> perfect HVAC; container_attenuation=1 ---> no HVAC")
print(f"   Spike magnitude now parameterized: spike_base_magnitude from CSV")









