# Cell 26: Reconstruct Usable Energy for Figure 1 (Duration Risk)
# =============================================================================
# This post-processing cell reconstructs deliverable-energy trajectories under
# the SAGE abstraction (Eqs. 4-7, 17) to quantify duration risk (usable-energy
# loss exceeding capacity fade).
#
# These are monthly state-of-health snapshots representing a counterfactual
# capability test: "if called at this moment, what constant-power energy could
# the asset deliver over the contractual duration H?"
#
# Because this is a firm 4-hour constant-power test, the asset can become
# energy-limited early even when SoH remains high, as soon as SOC-window
# contraction reduces E_avail·eta/H below nameplate power.
#
# Note: Thermal derating (Eqs. 8-10) is omitted in this reconstruction; if
# thermal limits bind, usable energy would be further reduced.

import numpy as np
import pandas as pd
from pathlib import Path
from collections.abc import Mapping

# -------------------------------------------------------------------------
# Parameter access helper (duck-typing for dict or object)
# -------------------------------------------------------------------------
def get_param(params, key):
    # """
    # Get a parameter value from either a dict-like or object-like params container.
    # """

    if isinstance(params, Mapping):
        if key not in params:
            raise KeyError(f"Parameter '{key}' not found in params dict.")
        return params[key]
    else:
        if not hasattr(params, key):
            raise AttributeError(f"Parameter '{key}' not found in params object.")
        return getattr(params, key)

# -------------------------------------------------------------------------
# 0. Get output directory and params
# -------------------------------------------------------------------------
PARAMS_FILE = require_global("PARAMS_FILE")
