def assert_close(actual, expected, name, rtol=RTOL, atol=ATOL):
    """Check if actual ~ expected within tolerance."""
    global tests_passed, tests_failed
    if np.isclose(actual, expected, rtol=rtol, atol=atol):
        tests_passed += 1
        print(f"   PASS: {name}")
