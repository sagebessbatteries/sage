def _float(p: dict, k: str) -> float:
    """Extract finite float or raise."""
    if k not in p:
        raise KeyError(f"Missing parameter: '{k}'")
    try:
        v = float(p[k])
    except (TypeError, ValueError) as e:
        raise ValueError(f"'{k}' must be numeric, got {p[k]!r}") from e
    if not np.isfinite(v):
        raise ValueError(f"'{k}' must be finite, got {v}")
    return v


def _int(p: dict, k: str) -> int:
    """Extract integer or raise."""
    v = _float(p, k)
    if abs(v - round(v)) > 1e-9:
        raise ValueError(f"'{k}' must be integer, got {v}")
    return int(v)


def _str(p: dict, k: str) -> str:
    """Extract string or raise."""
    if k not in p:
        raise KeyError(f"Missing parameter: '{k}'")
    return str(p[k]).strip()


def _list(p: dict, k: str) -> List[float]:
    """Extract list of floats or raise."""
    if k not in p:
        raise KeyError(f"Missing parameter: '{k}'")
    v = p[k]
    if isinstance(v, np.ndarray):
        return v.astype(float).tolist()
    if isinstance(v, (list, tuple)):
        return [float(x) for x in v]
    raise TypeError(f"'{k}' must be list, got {type(v).__name__}")


def _positive(v: float, k: str) -> float:
    if v <= 0:
        raise ValueError(f"'{k}' must be > 0, got {v}")
    return v


def _nonneg(v: float, k: str) -> float:
    if v < 0:
        raise ValueError(f"'{k}' must be >= 0, got {v}")
    return v


def _frac(v: float, k: str) -> float:
    if not 0.0 <= v <= 1.0:
        raise ValueError(f"'{k}' must be in [0,1], got {v}")
    return v


def _eff(v: float, k: str) -> float:
    if not 0.0 < v <= 1.0:
        raise ValueError(f"'{k}' must be in (0,1], got {v}")
    return v


