print(f"  E_usable:         {usable_energy_kwh[0]:.0f} kWh")

if eol_idx > 0:
    print(f"\nAt End of Life (month {df_traj['month'].iloc[eol_idx]}, year {years_vector[eol_idx]:.1f}):")
    print(f"  SoH:              {soh_vector[eol_idx]:.4f}")
    print(f"  SoH_norm:         {soh_norm[eol_idx]:.4f}")
