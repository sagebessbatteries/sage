# Run a short 48-hour trajectory and verify SOC stays within the
# logged soc_min/soc_max bounds at every timestep (Option B gold standard).
import pandas as pd

sp = sage_params
n_micro = 48  # 2 days

# Build minimal environment and command sequence
micro_start = pd.Timestamp("2025-01-01 00:00:00")
micro_env = []
micro_cmd = []

for h in range(n_micro):
    ts = micro_start + pd.Timedelta(hours=h)
    hour_of_day = ts.hour
    # Discharge during hours 14-17 (4-hour block)
    if 14 <= hour_of_day < 18:
        p_cmd = sp.P_dis
    else:
        p_cmd = 0.0

    micro_env.append(SAGEEnvironmentStep(
        t_timestamp=ts,
        p_market=50.0,
        p_forecast=50.0,
        T_ambient=22.0,
        T_amb_forecast=22.0,
        T_outdoor=30.0,
        T_outdoor_forecast=30.0,
        is_valid=True,
    ))
    micro_cmd.append(SAGECommand(P_dispatch_kw=p_cmd))

micro_init = SAGEState(
    soc_true=sp.SoC_max_BOL,
    soh_true=1.0,
    q_loss_cal=0.0,
    q_loss_cyc=0.0,
    T_cell=22.0,
    cumulative_kwh=0.0,
    t_index=0,
    t_cal_cumulative=0.0,
)

micro_traj, micro_final, _ = sage_simulate_trajectory(
    micro_init, micro_env, micro_cmd, sp,
    gradient_factor=0.5, quality_factor=1.0, verbose=False,
)

# Test 12.1: SOC within LOGGED window at every timestep (Option B)
# Tolerance matches Cell 5 idle_soc_tol = 1e-4: calendar aging contracts
# the SOC window each hour while idle SOC inherits from the daily reset.
# Over 20+ idle hours the accumulated window contraction can reach O(1e-5),
# so 1e-9 is too tight; 1e-4 matches the physics engine's own guard.
soc_tol_12 = 1e-4
soc_violations = 0
for t, state in enumerate(micro_traj):
    smin = state.soc_min
    smax = state.soc_max

    hard_assert(np.isfinite(smin) and np.isfinite(smax),
                f"Missing soc_min/soc_max at t={t}: {smin}, {smax}")

    if state.soc_true < smin - soc_tol_12 or state.soc_true > smax + soc_tol_12:
        soc_violations += 1

assert_true(soc_violations == 0,
            f"SOC within logged window (tol={soc_tol_12}) at all {len(micro_traj)} timesteps "
            f"(violations: {soc_violations})")

# Test 12.1b: Logged SOC bounds are well-ordered and within [0,1]
bounds_ok = all(0.0 <= s.soc_min < s.soc_max <= 1.0 for s in micro_traj)
assert_true(bounds_ok, "Logged SOC bounds well-ordered and within [0,1]")

idle_violations = 0
for t, state in enumerate(micro_traj):
    if t >= len(micro_cmd):
        break
    cmd_t = micro_cmd[t]
    if cmd_t.P_dispatch_kw == 0.0:
        if state.soc_true < state.soc_min - soc_tol_12 or state.soc_true > state.soc_max + soc_tol_12:
            idle_violations += 1
assert_true(idle_violations == 0,
            f"SOC within window during all idle hours (tol={soc_tol_12}, violations: {idle_violations})")

# Test 12.2: cumulative_kwh is monotonically non-decreasing and > 0
kwh_values = [s.cumulative_kwh for s in micro_traj]
kwh_monotonic = all(kwh_values[i+1] >= kwh_values[i] - 1e-12
                    for i in range(len(kwh_values)-1))
assert_true(kwh_monotonic, "cumulative_kwh is monotonically non-decreasing")
assert_true(kwh_values[-1] > 0, "cumulative_kwh > 0 after discharge")

