def _load_params(path: Path) -> pd.DataFrame:
    """
    Load parameter CSV with column validation.

    Parameters
    ----------
    path : Path
        Path to the CSV file.

    Returns
    -------
    pd.DataFrame
        Loaded parameter DataFrame.

    Raises
    ------
    ValueError
        If required columns are missing.
    """
    required_cols = {"name", "dtype", "value_float", "value_bool", "value_list", "value_str"}

    try:
        df = pd.read_csv(path, encoding="utf-8")
    except UnicodeDecodeError:
        df = pd.read_csv(path, encoding="ISO-8859-1")

    missing = required_cols - set(df.columns)
    if missing:
        raise ValueError(f"CSV missing columns: {sorted(missing)}")
    return df


def _parse_row(row: pd.Series) -> int | float | bool | list | str:
    """
    Parse a single parameter row into its typed value.

    Parameters
    ----------
    row : pd.Series
        Row from the parameter DataFrame.

    Returns
    -------
    int, float, bool, list, or str
        Parsed parameter value.

    Raises
    ------
    ValueError
        If dtype is unknown or value is missing/invalid.
    """
    name = str(row["name"])
    dtype = str(row["dtype"]).strip()

    if dtype == "int":
        if pd.isna(row["value_float"]):
            raise ValueError(f"'{name}': int parameter has no value")
        return int(row["value_float"])

    if dtype == "float":
        if pd.isna(row["value_float"]):
            raise ValueError(f"'{name}': float parameter has no value")
        return float(row["value_float"])

    if dtype == "bool":
        v = row["value_bool"]
        if isinstance(v, bool):
            return v
        s = str(v).strip().upper()
        if s == "TRUE":
            return True
        if s == "FALSE":
            return False
        raise ValueError(f"'{name}': invalid bool value '{v}'")

    if dtype == "list":
        raw = str(row["value_list"]).strip()
        if raw.lower() == "nan" or raw == "":
            return []
        s = raw.lstrip("[").rstrip("]").replace('"', "").strip()
        if not s:
            return []
        return [float(x.strip()) for x in s.split(",") if x.strip()]

    if dtype == "str":
        v = row.get("value_str")
        if pd.isna(v):
            v = row.get("value_list")
        if pd.isna(v):
            v = row.get("value_float")
        if pd.isna(v):
            raise ValueError(f"'{name}': str parameter has no value")
        return str(v).strip()

    raise ValueError(f"'{name}': unknown dtype '{dtype}'")


