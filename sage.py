@dataclass(frozen=True)
class PhysicsParams:
    """Physics-specific parameter subset. See Table S1."""

    cap_nom: float
    SoC_min_BOL: float
    SoC_max_BOL: float
    SoC_min_EOL: float
    SoC_max_EOL: float
    SoH_EOL: float
