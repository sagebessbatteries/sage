def estimate_sage_eol(years, soh, cumulative_kwh, soh_eol):
    """
    Return (t_eol_years, Q_eol_kwh) by locating/interpolating the first crossing soh <= soh_eol.
    Robust to:
      - exact equality at a row
      - crossing between rows
      - mild floating point noise
    """
    years = np.asarray(years, dtype=float)
    soh = np.asarray(soh, dtype=float)
    cumulative_kwh = np.asarray(cumulative_kwh, dtype=float)

    # If already exactly at/under EOL at last row, still compute first crossing
    idx = np.where(soh <= soh_eol)[0]
    if len(idx) == 0:
        raise ValueError(
            f"SAGE trajectory does NOT reach SoH_EOL={soh_eol} within the file horizon "
            f"(min SoH = {soh.min():.6f}, last SoH = {soh[-1]:.6f})."
        )

    i = int(idx[0])

    # If it hits at the first row, return that
    if i == 0:
        return years[0], cumulative_kwh[0]

    # If it hits exactly at row i (common if you clamp/stop exactly at EOL), return row i
    if np.isclose(soh[i], soh_eol, atol=1e-12):
        return years[i], cumulative_kwh[i]

    # Otherwise interpolate between i-1 and i
    t0, t1 = years[i-1], years[i]
    s0, s1 = soh[i-1], soh[i]
    q0, q1 = cumulative_kwh[i-1], cumulative_kwh[i]

    # s1 should be < s0 typically, so denominator nonzero
    denom = (s1 - s0)
    if abs(denom) < 1e-15:
        # Degenerate case: SoH flat across the interval, fall back to row i
        return years[i], cumulative_kwh[i]

    alpha = (soh_eol - s0) / denom  # should be in [0,1]
    alpha = float(np.clip(alpha, 0.0, 1.0))

    t_eol = t0 + alpha * (t1 - t0)
    q_eol = q0 + alpha * (q1 - q0)
    return t_eol, q_eol

# =============================================================================
# BASELINE MODELS (calibrated to TRUE SAGE EOL)
