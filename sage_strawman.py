def linear_degradation_model(years, soh_eol, t_eol_years):
    """Linear model: constant %/year degradation, calibrated to hit soh_eol at t_eol_years."""
    rate = (1.0 - soh_eol) / t_eol_years
    soh = 1.0 - rate * years
    return np.clip(soh, soh_eol, 1.0)

def throughput_degradation_model(cumulative_kwh, soh_eol, Q_eol_kwh):
    """Throughput-only model: degradation proportional to cumulative grid-side throughput."""
    k = (1.0 - soh_eol) / Q_eol_kwh
    soh = 1.0 - k * cumulative_kwh
    return np.clip(soh, soh_eol, 1.0)

