    years_all = df_single['years'].to_numpy(dtype=float)
    soh_sage_all = df_single['SoH_SAGE_Truth'].to_numpy(dtype=float)
    cumulative_kwh_all = df_single['cumulative_kwh'].to_numpy(dtype=float)

    t_eol_years, Q_eol_kwh = estimate_sage_eol(years_all, soh_sage_all, cumulative_kwh_all, SOH_EOL)

    mask = years_all <= (t_eol_years + 1e-12)
    years = years_all[mask]
    soh_sage = soh_sage_all[mask]
    cumulative_kwh = cumulative_kwh_all[mask]

    soh_linear = linear_degradation_model(years, SOH_EOL, t_eol_years)
    soh_throughput = throughput_degradation_model(cumulative_kwh, SOH_EOL, Q_eol_kwh)

    error_linear = (soh_linear - soh_sage) * 100
    error_throughput = (soh_throughput - soh_sage) * 100

    print("=" * 60)
    print("STRAWMAN COMPARISON STATISTICS (TRUE SAGE EOL)")
    print("=" * 60)

