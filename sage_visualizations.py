def mean_last_n_valid(arr, n=12):
    """
    Mean of last n valid (non-NaN) values in array.

    Used for computing mean temperature over last 12 months of operation,
    handling assets that reached EOL at different times.
    """
    a = np.array(arr, dtype=float)
    idx = np.where(np.isfinite(a))[0]
    if len(idx) == 0:
        return np.nan
    last = idx[-1]
    return np.nanmean(a[max(0, last - n + 1):last + 1])

