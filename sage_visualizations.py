# Hill estimator for fat tails
def hill_estimator(data, k):
    sorted_data = np.sort(data)[::-1]
    if k >= len(sorted_data) or k < 2:
        return np.nan
    top_k = sorted_data[:k]
    threshold = sorted_data[k]
    if threshold <= 0:
        return np.nan
    log_ratios = np.log(top_k / threshold)
    return k / np.sum(log_ratios)

