# Calculate projected life
def calc_projected_life(row):
    t_elapsed = row.get("years_survived", row.get("lifespan_years", sim_years))
    deg_consumed = 1.0 - row["final_soh"]
    if deg_consumed <= 0.001:
        return lifespan_cap
    ratio = deg_budget / deg_consumed
    return min(lifespan_cap, t_elapsed * (ratio ** (1.0 / beta_cal)))

df["Projected_Life"] = df.apply(calc_projected_life, axis=1)

