import numpy as np
import pandas as pd
from pathlib import Path

SAGE_DIR = Path("/content/drive/MyDrive/SAGE")
OUTDIR   = SAGE_DIR / "sensitivity_results_final"
PARAMS_CSV = SAGE_DIR / "sage_params.csv"
BASELINE_FLEET = SAGE_DIR / "sage_fleet_lifespans.csv"

assert OUTDIR.exists(), f"Missing: {OUTDIR}"
assert PARAMS_CSV.exists(), f"Missing: {PARAMS_CSV}"
assert BASELINE_FLEET.exists(), f"Missing: {BASELINE_FLEET}"

# ----------------------------
# Load baseline parameter values (p0) from sage_params.csv
# ----------------------------
params_df = pd.read_csv(PARAMS_CSV)

def get_baseline_param_value(name: str) -> float:
    row = params_df[params_df["name"].astype(str).str.strip() == name]
    if len(row) != 1:
        raise KeyError(f"Baseline parameter '{name}' not found uniquely in sage_params.csv")
    dtype = str(row.iloc[0]["dtype"]).strip().lower()
    if dtype in ["float", "int"]:
        return float(row.iloc[0]["value_float"])
    if dtype == "bool":
        return 1.0 if str(row.iloc[0]["value_bool"]).strip().upper() == "TRUE" else 0.0
    raise TypeError(f"Unsupported dtype for '{name}': {dtype}")

# ----------------------------
# Baseline mean lifespan L0 (for elasticity normalization)
# ----------------------------
L0 = float(pd.read_csv(BASELINE_FLEET)["lifespan_years"].mean())

# ----------------------------
# Parse sensitivity run files -> (Parameter, Value, MeanLife)
# ----------------------------
def parse_value_token(token: str) -> float:
    # encodes decimals like 0p02 -> 0.02
    return float(token.replace("p", "."))

def runname_from_filename(fname: str) -> str:
    return Path(fname).stem.replace("sage_fleet_lifespans_", "")

rows = []
fleet_files = sorted(OUTDIR.glob("sage_fleet_lifespans_*.csv"))
assert len(fleet_files) > 0, f"No sensitivity fleet files found in {OUTDIR}"

for f in fleet_files:
    run = runname_from_filename(f.name)
    parts = run.split("_")
    pname = "_".join(parts[:-1])
    pval = parse_value_token(parts[-1])

    df = pd.read_csv(f)
    mean_life = float(df["lifespan_years"].mean())

    rows.append({"Parameter": pname, "Value": pval, "MeanLife": mean_life})

df_all = pd.DataFrame(rows)

# ----------------------------
# Build summary: baseline param value p0, elasticity, range
# ----------------------------
summary = []

for pname, grp in df_all.groupby("Parameter"):
    grp = grp.sort_values("Value")
    x = grp["Value"].values.astype(float)
    y = grp["MeanLife"].values.astype(float)

    # Range in years (across sweep)
    life_range = float(np.max(y) - np.min(y))

    # Slope from OLS regression y = a + b x
    if len(x) >= 2:
        b, a = np.polyfit(x, y, 1)   # slope b = dL/dp
    else:
        b = np.nan

    # Baseline parameter value from sage_params.csv
    p0 = get_baseline_param_value(pname)

    # Elasticity at baseline: (p0/L0) * dL/dp
    elasticity = float((p0 / L0) * b) if np.isfinite(b) else np.nan

    summary.append({
        "Parameter": pname,
        "Baseline": p0,                 # <-- baseline PARAMETER VALUE (what you asked for)
        "Elasticity": elasticity,
        "Range (yr)": life_range
    })

df_summary = pd.DataFrame(summary)

# Optional: sort like a tornado table (largest impact first)
df_summary["AbsRange"] = df_summary["Range (yr)"].abs()
df_summary = df_summary.sort_values("AbsRange", ascending=False).drop(columns=["AbsRange"])

# Pretty rounding for SI
df_summary["Baseline"] = df_summary["Baseline"].map(lambda v: float(v))
df_summary["Elasticity"] = df_summary["Elasticity"].map(lambda v: float(v) if pd.notnull(v) else v)
df_summary["Range (yr)"] = df_summary["Range (yr)"].map(lambda v: float(v))

print("\nTornado summary (Parameter | Baseline param | Elasticity | Range yr)\n")
print(df_summary.round({"Baseline": 6, "Elasticity": 4, "Range (yr)": 4}).to_string(index=False))

# Save to Excel (1 sheet)
out_xlsx = OUTDIR / "SI_Tornado_Summary_Table.xlsx"
df_summary.round({"Baseline": 6, "Elasticity": 6, "Range (yr)": 6}).to_excel(out_xlsx, index=False)
print(f"\nSaved: {out_xlsx}")
print(f"\n(Elasticity uses L0={L0:.4f} yr from {BASELINE_FLEET.name})")

