for param in results_df['Parameter'].unique():
    subset = results_df[results_df['Parameter'] == param].sort_values('Value')
    x = subset['Value'].values
    y = subset['Mean (yr)'].values
    mid_idx = len(x) // 2
    x_base, y_base = x[mid_idx], y[mid_idx]
    slope = np.polyfit(x, y, 1)[0]
    elasticity = slope * (x_base / y_base) if y_base != 0 else 0
    elast_rows.append({
        'Parameter': param,
        'Baseline': x_base,
        'Elasticity': round(elasticity, 3),
        'Range (yr)': round(y.max() - y.min(), 2),
    })

elast_df = pd.DataFrame(elast_rows).sort_values('Elasticity', key=abs, ascending=False)

