assert SWEEPS_CSV.exists(), f"Missing: {SWEEPS_CSV}"
assert PARAMS_CSV.exists(), f"Missing: {PARAMS_CSV}"
assert BASELINE_FLEET.exists(), f"Missing: {BASELINE_FLEET}"

def pctl(x, q):
    return float(np.percentile(np.asarray(x, dtype=float), q))

def runname_from_filename(fname: str) -> str:
    return Path(fname).stem.replace("sage_fleet_lifespans_", "")

def parse_value_token(token: str) -> float:
    # encodes decimals like 0p02 -> 0.02
    return float(token.replace("p", "."))

def load_baseline_params(path: Path) -> dict:
    df = pd.read_csv(path)
    out = {}
    for _, r in df.iterrows():
        name = str(r["name"]).strip()
        dtype = str(r["dtype"]).strip().lower()
        if dtype in ["float", "int"]:
            out[name] = float(r["value_float"])
        elif dtype == "bool":
            out[name] = 1.0 if str(r["value_bool"]).strip().upper() == "TRUE" else 0.0
    return out

baseline_params = load_baseline_params(PARAMS_CSV)

# ----------------------------
# Table S7: sweep configurations
# ----------------------------
sweeps = pd.read_csv(SWEEPS_CSV)
rows_S7 = []
for _, r in sweeps.iterrows():
    pname = str(r["parameter"]).strip()
    base = baseline_params.get(pname, np.nan)
    values = [v.strip() for v in str(r["values"]).split(",")]
    for v in values:
        try:
            v_num = parse_value_token(v)
        except Exception:
            v_num = np.nan
        rows_S7.append({
            "Parameter": pname,
            "Baseline": base,
            "SweepValue": v_num if np.isfinite(v_num) else v
        })
table_S7 = pd.DataFrame(rows_S7).sort_values(["Parameter", "SweepValue"], kind="stable")

# ----------------------------
# Table S8: detailed fleet stats
# ----------------------------
fleet_files = sorted(OUTDIR.glob("sage_fleet_lifespans_*.csv"))
assert len(fleet_files) > 0, f"No sensitivity fleet files found in {OUTDIR}"

rows_S8 = []
for f in fleet_files:
    run = runname_from_filename(f.name)
    parts = run.split("_")
    pname = "_".join(parts[:-1])
    val = parse_value_token(parts[-1])

    df = pd.read_csv(f)
    lif = df["lifespan_years"].astype(float).values

    rows_S8.append({
        "Parameter": pname,
        "Value": val,
        "Mean": float(np.mean(lif)),
        "Std": float(np.std(lif)),
        "P10": pctl(lif, 10),
        "P50": pctl(lif, 50),
        "P90": pctl(lif, 90),
        "Min": float(np.min(lif)),
        "Max": float(np.max(lif)),
        "n": int(len(lif)),
        "source_file": f.name
    })

table_S8 = pd.DataFrame(rows_S8).sort_values(["Parameter", "Value"], kind="stable").reset_index(drop=True)

# ----------------------------
# Table S9: regression + elasticity
# ----------------------------
L0 = float(pd.read_csv(BASELINE_FLEET)["lifespan_years"].mean())

rows_S9 = []
for pname, grp in table_S8.groupby("Parameter"):
    x = grp["Value"].values.astype(float)
    y = grp["Mean"].values.astype(float)
    if len(x) < 2:
        continue

    slope, intercept = np.polyfit(x, y, 1)
    yhat = intercept + slope * x
    ss_res = float(np.sum((y - yhat) ** 2))
    ss_tot = float(np.sum((y - np.mean(y)) ** 2))
    r2 = 1.0 - ss_res / ss_tot if ss_tot > 0 else np.nan

    p0 = baseline_params.get(pname, np.nan)
    elasticity = (p0 / L0) * slope if np.isfinite(p0) else np.nan

    rows_S9.append({
        "Parameter": pname,
        "Baseline": p0,
        "Slope_dLdp": float(slope),
        "Intercept": float(intercept),
        "R2": float(r2),
        "Elasticity": float(elasticity),
        "Range_yr": float(np.max(y) - np.min(y)),
        "BaselineMeanLifespan_L0": float(L0),
        "n_points": int(len(x))
    })

table_S9 = pd.DataFrame(rows_S9).sort_values("Parameter", kind="stable").reset_index(drop=True)

